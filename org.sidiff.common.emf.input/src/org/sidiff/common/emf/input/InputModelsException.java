package org.sidiff.common.emf.input;

public class InputModelsException extends RuntimeException {

	private static final long serialVersionUID = 4203733231073276411L;

	public InputModelsException() {
	}

	public InputModelsException(String message, Throwable cause) {
		super(message, cause);
	}

	public InputModelsException(String message) {
		super(message);
	}

	public InputModelsException(Throwable cause) {
		super(cause);
	}
}
